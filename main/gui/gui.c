bool gui_init(lv_disp_t * p_display)
{
        bool success = (NULL != p_display);
        lv_theme_t * p_theme;

        if (success) {
                p_theme = lv_theme_mono_init(
                                p_display,
                                false,
                                &lv_font_montserrat_14);

                lv_disp_set_theme(p_display, p_theme);
        }

        return success;
}

